TileMap::TileMap() :
        water{TextureManager::loadTexture(asset("water"))},
        dirt{TextureManager::loadTexture(asset("dirt"))},
        grass{TextureManager::loadTexture(asset("grass"))},
        destRect{0, 0, 32, 32},
        map{} {

//    Load level 1's map
    loadMap(lvl1);
}

void TileMap::loadMap(int _map[20][25]) {
    for (int row = 0; row < 20; ++row)
        for (int column = 0; column < 25; ++column)
            map[row][column] = _map[row][column];
}

void TileMap::drawMap() {
    int type = 0;
    for (int row = 0; row < 20; ++row)
        for (int column = 0; column < 25; ++column) {
            type = map[row][column];

            destRect.x = column * 32;
            destRect.y = row * 32;

            switch (type) {
                case 0:
                    TextureManager::drawTexture(water, destRect);
                    break;
                case 1:
                    TextureManager::drawTexture(grass, destRect);
                    break;
                case 2:
                    TextureManager::drawTexture(dirt, destRect);
                    break;
            }
        }
}

